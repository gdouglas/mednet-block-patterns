<p style="font-size:16px">The &ldquo;in-house&rdquo; role is part of the new Embedded Counsellor Program (ECP), which provides students with direct access to resources that are tailored to their specific needs and concerns. We met with Asmae (pronounced Ess-maa) to learn more about the unique supports available for graduate students.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"style":{"typography":{"fontSize":"30px","letterSpacing":"-0.3px"}}} -->
<h2 style="font-size:30px;letter-spacing:-0.3px">Heading 2 Styling for Questions or Sections</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">My priority is to support graduate students with any challenges or difficulties they may be experiencing that might also be impacting their academic, personal and professional lives. The last two years have been difficult for many because of the added layer of stress, uncertainty and anxiety caused by the pandemic. When a student books an appointment, I&rsquo;ll work with them to assess their needs and decide what support is best moving forward. For some, this might be individual counselling or a range of other supports and services.</p>
<!-- /wp:paragraph -->

<!-- wp:quote {"className":"is-style-blue-quote"} -->
<blockquote class="wp-block-quote is-style-blue-quote"><p>As the embedded counsellor, I can connect students with the right supports and services they need to help them work through these challenges, while also giving them the tools they need to enrich their overall well-being.</p><cite>FirstName LastName, Title, Faculty</cite></blockquote>
<!-- /wp:quote -->

<!-- wp:heading {"style":{"typography":{"fontSize":"30px","letterSpacing":"-0.3px"}}} -->
<h2 style="font-size:30px;letter-spacing:-0.3px">What are some of the unique challenges faced by research graduate students?</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">Some challenges that we tend to see in the graduate student population is self-doubt or imposter syndrome; am I good enough to be here? Graduate students tend to set very high expectations for themselves and compare themselves to their peers or lab mates which can create an enormous amount of stress and anxiety. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">They also navigate and juggle many responsibilities and relationships which can include being a parent, a professional, a partner and a student. It can be quite challenging to find a balance in all of these roles. Some students also reach out for support on how to communicate more effectively with their supervisors.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"style":{"typography":{"fontSize":"30px","letterSpacing":"-0.3px"}}} -->
<h2 style="font-size:30px;letter-spacing:-0.3px">How can supervisors help their students?</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">In addition to my role, UBC has many wonderful resources which can be accessed on our <a href="/" target="_blank" rel="noreferrer noopener">Graduate Student Wellbeing</a> page. The site offers information on how to book an appointment, upcoming well-being events and workshops, self-help and crisis support tools, as well as our Wellbeing Blog.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"style":{"typography":{"fontSize":"30px","letterSpacing":"-0.3px"}}} -->
<h2 style="font-size:30px;letter-spacing:-0.3px">How can supervisors help their students?</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">Supervisors can help their students by normalizing having conversations around mental health and well-being, and reminding students of the many UBC resources available to them, including the Embedded Counsellor Program. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">When meeting with students, a supervisor can also check in and remind students of the importance of self-care and highlight the importance of prioritizing one&rsquo;s well-being. I encourage supervisors to create self-care goals with their students, in addition to academic/work goals. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"fontSize":"16px"}}} -->
<p style="font-size:16px">Lastly, one very simple action that can have a significant impact is to validate a student&rsquo;s experience. If a student shares that they are struggling, we want them to feel heard and validated. This will help with building and maintaining a healthy supervisor-supervisee relationship, which is one of the factors that can contribute positively to a graduate student&rsquo;s well-being and success in graduate school.</p>
<!-- /wp:paragraph -->

